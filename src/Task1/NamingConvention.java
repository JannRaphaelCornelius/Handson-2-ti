package Task1;

public class NamingConvention {
    public static void main(String[] args) {
        // Buatlah variabel dengan nama yang mengikuti camelCase
        // untuk data mahasiswa berikut:
        // - nama lengkap mahasiswa
        // - nomor induk mahasiswa
        // - IPK semester lalu
        // - status aktif kuliah
        // - alamat rumah
        // - jumlah SKS yang diambil

        // Contoh:
        String namaLengkapMahasiswa = "Andi Wijaya";

        // Lanjutkan untuk variabel lainnya...
        String namaLengkapmahasiswa = "Jann Raphael Cornelius Silalahi";
        int nomorIndukMahasiswa = 2481025;
        double ipkSemesterLalu = 3.85;
        boolean isActive = true;
        String alamatRumah = "JL. Anggrek, blok A no 168, Jakamulya, Bekasi Selatan";
        int jumlahSks = 19;

        // Tampilkan semua data
        System.out.println("=== DATA MAHASISWA ===");
        System.out.println("Nama Lengkap: " + namaLengkapmahasiswa);
        System.out.println("NIM: " + nomorIndukMahasiswa);
        System.out.println("IPK semester lalu: " + ipkSemesterLalu);
        System.out.println("Status aktif kuliah: " + isActive);
        System.out.println("Alamat rumah: " + alamatRumah);
        System.out.println("Jumlah sks: " + jumlahSks);

        // Lengkapi output di sini
    }
}